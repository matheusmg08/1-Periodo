#Faça um algoritmo que receba dois números e ao final mostre a soma, subtração, multiplicação e a divisão dos números lidos.
print('=' *50)
primeiro_numero = float(input('Digite o primeiro número: '))
segundo_numero = float(input('Digite o segundo número: '))
soma = primeiro_numero + segundo_numero
subtracao = primeiro_numero - segundo_numero
multiplicacao = primeiro_numero * segundo_numero
divisao = primeiro_numero / segundo_numero
print(f'A soma entre os numeros é {soma} , a subtração é {subtracao} , a multiplicação é {multiplicacao} e a divisão entrele eles é {divisao} . ')


