# Entrada de dados
nome = input('Digite o nome: ')
idade = int(input('Digite a idade: '))
altura = float(input('Digite a altura: '))
peso = float(input('Digite o peso: '))
nacionalidade = input('Digite a nacionalidade: ')
# Processamento
paragrafo = f'O nome é {nome} , tem {idade} anos , mede {altura:.2f} m , o peso é {peso} kg. e a nacionalidade é {nacionalidade} !'
# Saída de dados
print (paragrafo)




